package com.yourapp.application.dto.response

import java.util.UUID

/**
 * DTO для представления книги в ответах API.
 *
 * Скрывает внутренние детали доменной модели от клиента.
 * Включает агрегированные данные о книге и ее главах.
 */
data class BookResponse(
    /**
     * Уникальный идентификатор книги.
     */
    val id: UUID,

    /**
     * Название книги.
     */
    val title: String,

    /**
     * Автор книги.
     */
    val author: String,

    /**
     * Язык книги.
     */
    val language: String,

    /**
     * URL обложки книги.
     */
    val coverUrl: String?,

    /**
     * Флаг наличия аудиоверсии.
     * Рассчитывается на основе наличия аудио в хотя бы одной главе.
     */
    val hasAudio: Boolean,

    /**
     * Флаг наличия текстовой версии.
     * Рассчитывается на основе наличия текста в хотя бы одной главе.
     */
    val hasText: Boolean,

    /**
     * Общее количество глав в книге.
     */
    val chapterCount: Int,

    /**
     * Список глав книги (упрощенный вид).
     * Для полной информации о главе использовать отдельный эндпоинт.
     */
    val chapters: List<Chapter>
)
)